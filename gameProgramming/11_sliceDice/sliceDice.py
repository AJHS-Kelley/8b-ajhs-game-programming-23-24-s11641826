print(alphabet[0])
print(alphabet[-1])

# Slicing
print(alphabet[0:4]) # Slice up to second index.
# alphabet[start:stop]

# slice from the start
print(alphabet[:7])

# slice to the end
print(alphabet[12:])

# slice the whole %#$#! thing
print(alphabet[:])

# negavtive index slicing
print(alphabet[-4:-1])


